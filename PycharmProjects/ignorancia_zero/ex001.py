n1 = int(input('Digite um numero: '))
n2 = int(input('Digite um numero: '))
print('{} + {} = {}'.format(n1, n2, n1 + n2))
print('{} - {} = {}'.format(n1, n2, n1 - n2))
print('{} x {} = {}'.format(n1, n2, n1 * n2))
print('{} / {} = {:.1f}'.format(n1, n2, n1 / n2))
print('{} // {} = {}'.format(n1, n2, n1 // n2))
print('{} resto {} = {}'.format(n1, n2, n1 % n2))






